const getFlagEmoji = (countryCode) => {
    if (!countryCode) return '';
    const codePoints = countryCode
        .toUpperCase()
        .split('')
        .map(char => 127397 + char.charCodeAt());
    return String.fromCodePoint(...codePoints);
};

let lines = content.split('\n');
let replacedHtml = 0;

for (let i = 0; i < lines.length; i++) {
    if (lines[i].includes('bandeira: ""')) {
        let nameMatch = lines[i].match(/nome: "([^"]+)"/);
        if (nameMatch) {
            let nName = nameMatch[1];
            let iso2 = iso2Map[nName];
            if (iso2) {
                lines[i] = lines[i].replace('bandeira: ""', `bandeira: "${getFlagEmoji(iso2)}"`);
            } else {
                lines[i] = lines[i].replace('bandeira: ""', `bandeira: "ðŸ—ºï¸"`);
            }
            replacedHtml++;
        }
    }
}

content = lines.join('\n');

fs.writeFileSync(targetFile, content);
